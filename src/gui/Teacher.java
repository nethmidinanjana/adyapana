    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String nic = jTextField3.getText();
        String fname = jTextField1.getText();

        if (nic.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Type NIC !", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Couldn'd find your NIC !", "Warning", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                ResultSet resultset = MySQL.execute("SELECT * FROM teacher WHERE nic='" + nic + "' ");

                if (resultset.next()) {
                    String teacherID = resultset.getString("teacher.id");

                    try {

                        MySQL.execute("UPDATE teacher SET `status_id`='2' WHERE teacher.id = '" + teacherID + "' ");

                        JOptionPane.showMessageDialog(this, "Teacher Deactivated !", "Warning", JOptionPane.ERROR_MESSAGE);
                        reset();
                        jDateChooser1.setEnabled(true);
                        jButton1.setEnabled(true);

                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

