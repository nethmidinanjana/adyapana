    private void jTextField3KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyReleased

        String nic = jTextField3.getText();
        jTextField1.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField2.setText("");
        jTextField4.setText("");

        jDateChooser1.setEnabled(true);
        jButton1.setEnabled(true);

        if (nic.length() >= 10) {
            try {
                ResultSet resultset = MySQL.execute("SELECT * FROM `teacher` WHERE `nic`='" + nic + "' ");

                if (resultset.next()) {

                    jTextField1.setText(resultset.getString("teacher.first_name"));
                    jTextField2.setText(resultset.getString("teacher.last_name"));
                    jTextField4.setText(resultset.getString("teacher.address"));
                    jDateChooser1.setEnabled(false);
                    jTextField6.setText(resultset.getString("teacher.mobile"));
                    jTextField7.setText(resultset.getString("teacher.email"));

                    jButton1.setEnabled(false);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jTextField3KeyReleased

