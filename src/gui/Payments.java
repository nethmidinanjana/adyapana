    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        String nic = jTextField1.getText();
        String fname = jTextField2.getText();
        String lname = jTextField3.getText();

        String clas_no = jTextField4.getText();

        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM class INNER JOIN teacher ON class.teacher_id = teacher.id\n"
                    + "INNER JOIN subject ON class.subject_sub_no = subject.sub_no WHERE class.class_no='" + clas_no + "'");

            if (resultset.next()) {

                String subject = resultset.getString("subject.name");
                String tfname = resultset.getString("teacher.first_name");
                String tlname = resultset.getString("teacher.last_name");
                String fullname = tfname + " " + tlname;

                String from = resultset.getString("class.from");
                String to = resultset.getString("class.to");
                String timeslot = from + " - " + to;
                String payment = resultset.getString("class.payment");

                try {

                    HashMap<String, Object> parameters = new HashMap<>();
                    parameters.put("nic", nic);
                    parameters.put("fname", fname);
                    parameters.put("lname", lname);

                    //JREmptyDataSource dataSource = new JREmptyDataSource();
                    Details details = new Details();
                    details.setSubject(subject);
                    details.setTeacher(fullname);
                    details.setTimeslot(timeslot);
                    details.setPayment(payment);

                    Vector<Details> vector = new Vector<>();
                    vector.add(details);

                    JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(vector);

                    JasperPrint report = JasperFillManager.fillReport("src/reports/Adyapana.jasper", parameters, dataSource);

                    JasperViewer.viewReport(report, false);

                } catch (Exception e) {
                    e.printStackTrace();
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton3ActionPerformed

