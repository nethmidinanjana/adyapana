    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased

        String cls_no = jTextField4.getText();

        jComboBox1.setSelectedIndex(0);
        jComboBox2.setSelectedIndex(0);
        jComboBox3.setSelectedIndex(0);
        jTextField5.setText("");

        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `class` INNER JOIN teacher ON class.teacher_id = teacher.id\n"
                    + "INNER JOIN subject ON class.subject_sub_no = subject.sub_no WHERE `class_no`='" + cls_no + "' ");

            if (resultset.next()) {

                String firstName = resultset.getString("teacher.first_name");
                String lastName = resultset.getString("teacher.last_name");
                String fullName = firstName + " " + lastName;

                String selectedValue = fullName;
                for (int i = 0; i < jComboBox2.getItemCount(); i++) {
                    String item = jComboBox2.getItemAt(i);
                    if (selectedValue.equals(item)) {
                        jComboBox2.setSelectedIndex(i);
                        break;
                    }
                }

                String sv = resultset.getString("subject.name");
                for (int x = 0; x < jComboBox1.getItemCount(); x++) {
                    String itm = jComboBox1.getItemAt(x);
                    if (sv.equals(itm)) {
                        jComboBox1.setSelectedIndex(x);
                        break;
                    }
                }

                String from = resultset.getString("class.from");
                String to = resultset.getString("class.to");
                String timeslt = from + " - " + to;

                String timeslot = timeslt;
                for (int j = 0; j < jComboBox3.getItemCount(); j++) {
                    String timeitm = jComboBox3.getItemAt(j);
                    if (timeslot.equals(timeitm)) {
                        jComboBox3.setSelectedIndex(j);
                        break;
                    }
                }

                jTextField5.setText(resultset.getString("class.payment"));

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jTextField4KeyReleased

