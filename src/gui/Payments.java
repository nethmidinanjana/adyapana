    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String student = jTextField1.getText();
        String class_no = jTextField4.getText();

        int StudentId = studentMap.get(student);

        //registered date
        Date date = new Date();
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String stringDate = format.format(date);

        if (student.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Type NIC !", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (class_no.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Type Class Number !", "Warning", JOptionPane.ERROR_MESSAGE);
        } else {

            try {
                ResultSet resultset = MySQL.execute("SELECT * FROM `student_has_class` WHERE `student_id`='" + StudentId + "' AND `class_class_no`= '" + class_no + "' ");

                if (resultset.next()) {

                    try {
                        ResultSet rs = MySQL.execute("SELECT * FROM class INNER JOIN teacher ON class.teacher_id = teacher.id\n"
                                + "INNER JOIN subject ON class.subject_sub_no = subject.sub_no WHERE class.class_no='" + class_no + "'  ");

                        if (rs.next()) {
                            String subjectId = rs.getString("subject.sub_no");
                            String teacher_id = rs.getString("teacher.id");
                            MySQL.execute("INSERT INTO `invoice`(`subject_sub_no`,`class_class_no`,`teacher_id`,`student_id`,`date_time`)"
                                    + "VALUES ('" + subjectId + "','" + class_no + "','" + teacher_id + "','" + StudentId + "','" + stringDate + "') ");

                            JOptionPane.showMessageDialog(this, "Payment Success", "Success", JOptionPane.DEFAULT_OPTION);

                        }

                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "This student is not regitered for this class !", "Warning", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
            }

        }


    }//GEN-LAST:event_jButton2ActionPerformed

