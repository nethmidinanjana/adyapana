    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String subject = String.valueOf(jComboBox3.getSelectedItem());
        String newSubject = jTextField2.getText();

        if (subject.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Choose a subject first !", "Warning", JOptionPane.ERROR_MESSAGE);
        } else if (newSubject.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Add new subject name !", "Warning", JOptionPane.ERROR_MESSAGE);
        } else {

            try {
                MySQL.execute("UPDATE `subject` SET `name`='" + newSubject + "' WHERE `subject`.`name`='" + subject + "' ");

                JOptionPane.showMessageDialog(this, "Successfully updated !", "Success", JOptionPane.DEFAULT_OPTION);

                jComboBox3.setSelectedIndex(0);
                jTextField2.setText(" ");

            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

