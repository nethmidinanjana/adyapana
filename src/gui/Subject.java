    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String subject = jTextField1.getText();

        if (subject.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter the subject name !", "Warning", JOptionPane.ERROR_MESSAGE);
        } else {

            try {
                ResultSet resultSet = MySQL.execute("SELECT COUNT(*) FROM subject WHERE `name`='" + subject + "' ");

                if (resultSet.next()) {
                    int count = resultSet.getInt(1);
                    if (count > 0) {
                        // The entered text exists in the database
                        JOptionPane.showMessageDialog(this, "This subject is already exists !", "Warning", JOptionPane.ERROR_MESSAGE);
                    } else {
                        // The entered text is not present in the database
                        MySQL.execute("INSERT INTO `subject`(`name`) VALUES('" + subject + "')");

                        jTextField1.setText("");

                        JOptionPane.showMessageDialog(this, "Subject successfully added ", "Success", JOptionPane.PLAIN_MESSAGE);
                    }
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

