    private void loadTable() {
        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM class INNER JOIN teacher ON class.teacher_id = teacher.id\n"
                    + "INNER JOIN subject ON class.subject_sub_no = subject.sub_no");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultset.next()) {

                Vector<String> v2 = new Vector<>();

                String column1 = resultset.getString("class.from");
                String column2 = resultset.getString("class.to");

                String fname = resultset.getString("teacher.first_name");
                String lname = resultset.getString("teacher.last_name");

                v2.add(resultset.getString("class.class_no"));
                v2.add(resultset.getString("subject.name"));
                v2.add(fname + " " + lname);
                v2.add(column1 + " - " + column2);
                v2.add(resultset.getString("class.payment"));

                model.addRow(v2);
                jTable1.setModel(model);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadSubject() {
        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `subject`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultset.next()) {
                vector.add(resultset.getString("subject.name"));
                studentMap.put(resultset.getString("subject.name"), resultset.getInt("subject.sub_no"));
            }

            jComboBox3.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadTeacher() {
        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `teacher`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultset.next()) {
                String column1 = resultset.getString("teacher.first_name");
                String column2 = resultset.getString("teacher.last_name");

                vector.add(column1 + " " + column2);

            }

            jComboBox1.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

