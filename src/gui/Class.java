    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased

        String cls_no = jTextField4.getText();

        jComboBox1.setSelectedIndex(0);
        jComboBox3.setSelectedIndex(0);
        timePicker1.setTime(null);
        timePicker2.setTime(null);
        jTextField1.setText("");
        jButton1.setEnabled(true);

        try {
            ResultSet resultset = MySQL.execute("SELECT * FROM `class` INNER JOIN teacher ON class.teacher_id = teacher.id\n"
                    + "INNER JOIN subject ON class.subject_sub_no = subject.sub_no WHERE `class_no`='" + cls_no + "' ");

            if (resultset.next()) {

                String firstName = resultset.getString("teacher.first_name");
                String lastName = resultset.getString("teacher.last_name");
                String fullName = firstName + " " + lastName;

                String selectedValue = fullName;
                for (int i = 0; i < jComboBox1.getItemCount(); i++) {
                    String item = jComboBox1.getItemAt(i);
                    if (selectedValue.equals(item)) {
                        jComboBox1.setSelectedIndex(i);
                        break;
                    }
                }
                
                
                String sv = resultset.getString("subject.name");
                for (int x = 0; x < jComboBox3.getItemCount(); x++) {
                    String itm = jComboBox3.getItemAt(x);
                    if (sv.equals(itm)) {
                        jComboBox3.setSelectedIndex(x);
                        break;
                    }
                }

                //from
                String timeString = resultset.getString("class.from");
                LocalTime time = LocalTime.parse(timeString);
                timePicker1.setTime(time);

                //to
                String time2 = resultset.getString("class.to");
                LocalTime t = LocalTime.parse(time2);
                timePicker2.setTime(t);

                jTextField1.setText(resultset.getString("class.payment"));

                jButton1.setEnabled(false);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jTextField4KeyReleased

